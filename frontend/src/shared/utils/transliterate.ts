/**
 * Транслитерация русского текста в латиницу для URL
 */
export const transliterate = (text: string): string => {
    const map: Record<string, string> = {
        'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo',
        'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm',
        'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
        'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'sch',
        'ъ': '', 'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya',
        'А': 'a', 'Б': 'b', 'В': 'v', 'Г': 'g', 'Д': 'd', 'Е': 'e', 'Ё': 'yo',
        'Ж': 'zh', 'З': 'z', 'И': 'i', 'Й': 'y', 'К': 'k', 'Л': 'l', 'М': 'm',
        'Н': 'n', 'О': 'o', 'П': 'p', 'Р': 'r', 'С': 's', 'Т': 't', 'У': 'u',
        'Ф': 'f', 'Х': 'h', 'Ц': 'ts', 'Ч': 'ch', 'Ш': 'sh', 'Щ': 'sch',
        'Ъ': '', 'Ы': 'y', 'Ь': '', 'Э': 'e', 'Ю': 'yu', 'Я': 'ya',
    };

    // Транслитерируем все символы, удаляя пустые строки (мягкие/твердые знаки)
    let result = text
        .split('')
        .map((char) => {
            if (map[char] !== undefined) {
                return map[char];
            }
            // Если это латинская буква или цифра, оставляем как есть
            if (char.match(/[a-zA-Z0-9]/)) {
                return char;
            }
            // Все остальное (пробелы, знаки препинания) заменяем на пробел
            return ' ';
        })
        .filter((str) => str !== '') // Удаляем пустые строки (от мягких/твердых знаков)
        .join('')
        .toLowerCase();

    // Заменяем пробелы на дефисы, убираем множественные дефисы
    result = result
        .replace(/\s+/g, '-') // Пробелы на дефисы
        .replace(/-+/g, '-') // Множественные дефисы в один
        .replace(/^-+|-+$/g, ''); // Убираем дефисы в начале и конце

    return result;
};
